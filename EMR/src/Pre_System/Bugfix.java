package Pre_System;

public class Bugfix {
	
	private String Path;
	private String Bugfix;
	private Vector RepresentativeBugfixVector;
	
	public Vector getRepresentativeBugfixVector() {
		return RepresentativeBugfixVector;
	}
	public void setRepresentativeBugfixVector(Vector representativeBugfixVector) {
		RepresentativeBugfixVector = representativeBugfixVector;
	}
	public String getBugfix() {
		return Bugfix;
	}
	public void setBugfix(String bugfix) {
		Bugfix = bugfix;
	}
	public String getPath() {
		return Path;
	}
	public void setPath(String path) {
		Path = path;
	}
}
