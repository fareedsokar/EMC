package HandlingNewBugfix;

public class Bugfix
{
	private String Path;
	private String bugfix;
	private Vector RepresentBugfixVector;
	
	
	public String getPath() {
		return Path;
	}
	public void setPath(String path) {
		Path = path;
	}
	public String getBugfix() {
		return bugfix;
	}
	public void setBugfix(String bugfix) {
		this.bugfix = bugfix;
	}
	public Vector getRepresentBugfixVector() {
		return RepresentBugfixVector;
	}
	public void setRepresentBugfixVector(Vector representBugfixVector) {
		RepresentBugfixVector = representBugfixVector;
	}
	
	
	
	
	
}
